package cn.enaium.nurupoga.controller

import cn.enaium.nurupoga.annotation.Optional
import cn.enaium.nurupoga.annotation.OptionalBody
import cn.enaium.nurupoga.model.wrapper.ResultWrapper
import cn.enaium.nurupoga.service.GiteeService
import org.springframework.web.bind.annotation.PostMapping
import org.springframework.web.bind.annotation.RequestMapping
import org.springframework.web.bind.annotation.RestController

/**
 * @author Enaium
 */
@RestController
class GiteeController(val giteeService: GiteeService) {
    /**
     * gitee auth
     *
     * @param code auth code
     * @param type idk
     */
    @OptionalBody
    @PostMapping("/auth")
    fun auth(@Optional code: String, @Optional type: String?): ResultWrapper<Any> {
        return giteeService.auth(code, type)
    }
}